public class eksplorevariable{
    public static void main(String[] args) {
        // integer
        int umur = 12;
        System.out.println("umur anda = " + umur);

        // floting pint (float)
        float berat = (float) 75.9;
        System.out.println("berat kamu = " + berat + "kg");

        // double floating pint (double)
        double panjang = 10;
        System.out.println("panjang = " + panjang + " meter");

        // string
        String nama = "difo armi frasetya";
        System.out.println("nama saya : " + nama);

        // karakter (char)
        char gender = 'l';
        System.out.println("gender = " + gender);

        //boolen (boolean)
        boolean sehat = true ;
        System.out.println(sehat);

    }
}